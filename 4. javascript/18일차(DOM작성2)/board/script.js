function writeSave(){
    //작성자
    if(document.writeform.writer.value==""){
        alert('작성자를 먼저 입력');
        document.writeform.writer.focus();
        return false;
    }

    if(document.writeform.subject.value==""){
        alert('제목을 먼저 입력');
        document.writeform.subject.focus();
        return false;
    }

    if(document.writeform.writer.value==""){
        alert('작성자를 먼저 입력');
        document.writeform.writer.focus();
        return false;
    }

    if(document.writeform.writer.value==""){
        alert('작성자를 먼저 입력');
        document.writeform.writer.focus();
        return false;
    }

    if(document.writeform.writer.value==""){
        alert('작성자를 먼저 입력');
        document.writeform.writer.focus();
        return false;
    }
}